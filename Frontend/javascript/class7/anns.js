// push():
let color =["white","black","pink","red"];
console.log(color);
let addcolor ="blue";
color.push(addcolor);
console.log(color);

//pop():
let subject =["phy","math","eng","hst"];
console.log(subject);
subject.pop();
console.log(subject);

//shift():
let number =["45","67","89","90"];
console.log(number);
number.shift();
console.log(number);

//unshift()
let names =["sonali","prangya","anlita","sudi"];
console.log(names);
names.unshift();
console.log(names);
